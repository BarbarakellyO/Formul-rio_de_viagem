var bebida = "Leite";
var valor = "";

switch(bebida) {

    
    case bebida = "Café":
        valor = 2;
        
    case bebida = "Chá":
        valor = 10;
        
    case bebida = "Leite":
        valor = 8;

    
        console.log(`${bebida} custa R$ ${valor},00.`);
        break;
    
    default:
    console.log(`Escolha inválida. Por favor, escolha entre café, leite ou chá.`) }